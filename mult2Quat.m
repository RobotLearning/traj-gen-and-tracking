% Function to multiply two quaternions

function q3 = mult2Quat(q1,q2)

    q3(1) = q1(1)*q2(1) - ...
            q1(2:4)*q2(2:4);
    q3(2) = q1(2)*q2(1) + ...
            q1(1)*q2(2) + ...
            q1(3)*q2(4) - ...
            q1(4)*q2(3);
    q3(3) = q1(1)*q2(3) - ...
            q1(2)*q2(4) + ...
            q1(3)*q2(1) + ...
            q1(4)*q2(2);
    q3(4) = q1(1)*q2(4) + ...
            q1(2)*q2(3) - ...
            q1(3)*q2(2) + ...
            q1(4)*q2(1);


end